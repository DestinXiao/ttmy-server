package com.maple.game.osee.dao.log.entity.gm;

/**
 * 分组计数类
 */
public class GroupCount {

	/**
	 * 分组键
	 */
	private Object key;

	/**
	 * 数量
	 */
	private int count;

	public Object getKey() {
		return key;
	}

	public void setKey(Object key) {
		this.key = key;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}
